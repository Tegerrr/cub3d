#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))

{
	t_list	*a;

	if (!lst || !f)
		return ;
	a = lst;
	while (a)
	{
		(*f)(a -> content);
		a = a -> next;
	}
}
