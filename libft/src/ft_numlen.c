int	ft_numlen(int n)

{
	int	i;

	i = 0;
	if (n < 0)
	{
		n *= -1;
		i++;
	}
	while (n / 10 > 0)
	{
		n = n / 10;
		i++;
	}
	if (n <= 9)
		i++;
	return (i);
}
