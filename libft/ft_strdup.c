#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *src)
{
	char	*dup;

	dup = (char *)malloc (ft_strlen(src) + 1);
	if (dup == 0)
		return (0);
	ft_strcpy(dup, src);
	return (dup);
}
